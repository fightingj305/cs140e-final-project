int main() {

    UART_Disable();
    UART_Config(&uart);
    UART_Enable();
    UART_Send_String("Program start\n");

    I2S_Init(&i2s);

    UART_Send_String("I2S Initialized\n");


    UART_Send_String("I2S Enabled\n");


    // Pre-fill FIFO before starting loop
    for (int i = 0; i < 16; i++) {
        PUT32(PCM_FIFO_A, 0);
    }

    I2S_Clear_Flags();  // clear TXERR accumulated during init
    I2S_Enable_Comms();
    uint32_t phase = 0;

    while (1) {

        int32_t sample = cos_table[phase];

        I2S_Send_Value(sample);   // left channel
        // right channel automatically zero in your driver
        // UART_Send_String("I2S Sent\n");

        phase += phase_step;
        if (phase >= TABLE_SIZE)
            phase -= TABLE_SIZE;
    }

    return 0;
}